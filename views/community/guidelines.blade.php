<p>On PatchChat, users should: 
    <ol>
        <li>Not make attacks or otherwise disparage other users or their content.</li>
        <li>Be respectful and considerate, and assume that other people on the site are also trying to make it a great resource and community.</li>
        <li>Respect opposing or differing opinions, beliefs and conclusions.</li>
        <li>Attempt to listen to and understand others you may disagree with.</li>
        <li>Avoid profane, offensive, and sex-related language when possible.</li>
        <li>Encourage others on the site to also be welcoming and respectful.</li>            
    </ol>    
</p>

<h3>Overview</h3>

<p>The core PatchChat principle (<strong>&ldquo;Treat Others the Way You Want to Be Treated&rdquo;</strong>) requires that people treat other people on the site with civility, respect, and consideration. Assume that others on the site are also trying to make it a great community. Try to listen to and understand others you may disagree with. Encourage others on the site to also be welcoming and respectful.</p>

<h3>Attacking people or content</h3>

<p>Personal attacks are not allowed on PatchChat, nor are disrespectful or insulting attacks directed at other people's content. People should be civil and respectful in disagreement with others and should not: <ol>
    <li>Make attacks or otherwise disparage other people.</li>
    <li>Refer to other people's content with insulting or disrespectful language.</li>
    <li>Harass others on the site. These behaviours hurt the PatchChat community and deter users from helping to create a better resource.</li>
</ol>
</p>

<h3>Comment threads & how to disagree in comments</h3>

<p>In general, comment threads on PatchChat are interactions among strangers. Given that another person on the site may be new to PatchChat and/or doesn't know you, we require a higher level of politeness than other interactive platforms where users know one another and/or where more adversarial social norms are established and tolerated. The goal of the <strong>Treat Others the Way You Want to Be Treated principle</strong> is to ensure that comments do <strong>not</strong> discourage or intimidate other people on PatchChat.</p>

<p>In multi-comment threads where there is significant disagreement among people, a person should stop commenting on the thread before creating the reasonable impression that they are harassing, attacking, and/or bullying another person.</p>

<h3>Examples</h3>

<p></p>Examples of behaviours that violate the <strong>&ldquo;Treat Others the Way You Want to Be Treated&rdquo;</strong> policy include: 

<OL>
	<li>Personal attacks, including hurtful, insulting, or hostile comments.</li>
	<li>Making unjustified accusations against other user(s).</li>
	<li>Engaging in a pattern of behaviour that constitutes harassment of a targeted person or persons, e.g., by making threats, repeated annoying and unwanted contacts, repeated personal attacks, or posting personal information.</li>
	<li>Racial, sexual, homophobic, ageist, religious, political, ethnic, or other epithets directed against another contributor.</li>
	<li>Using someone's affiliations as a means of dismissing or discrediting his or her views (this does not include pointing out a conflict of interest of relevance to the discussion at hand).</li>
	<li>Speculating on the real life identity of an anonymous user.</li>
</OL>

<h3>Harassment</h3>

<p>Harassment is defined as offensive behaviour that appears to a reasonable observer to have the purpose of adversely affecting a targeted person or persons. The intended outcome may be to make using PatchChat unpleasant for the target, to undermine them, or to discourage them from editing and using the site. Examples of harassment include (but are not limited to): 
<ol>
    <li>Making threats.</li>
    <li>Repeated annoying and unwanted contacts.</li>
    <li>Posting the personal information of another person.</li>
    <li>Using sexually explicit, profane, adversarial, or flirtatious language toward another person if such language would likely make the person uncomfortable.</li>    
</ol>
</p>

<h3>Hate speech</h3>
<p>Users are not allowed to post content or adopt a tone that would be interpreted by a reasonable observer as a form of hate speech, particularly toward a race, gender, religion, nationality, ethnicity, political group, sexual orientation or another similar characteristic. Questions and question details about generalizations in these topics should be phrased as neutrally and respectfully as possible.</p>

<h3>Racial and ethnic slurs</h3>

<p>Using any of the words on Wikipedia's <a target=_blank href="http://en.wikipedia.org/wiki/List_of_ethnic_slurs">list of ethnic slurs</a> is not allowed in questions, answers, or comments.</p>

<h3>Messages</h3>

<p>The core PatchChat principle (<strong>&ldquo;Treat Others the Way You Want to Be Treated&rdquo;</strong>) applies to Messages on PatchChat.</p>

<h3>Consequences</h3>
<p>
    <ul>
        <li>Content that violates this policy may be reported to and removed by admins,
and violations of this policy can result in a warning and or be banned.</li>
        <li>Depending on the severity of the violation, a user may be banned
immediately (i.e., without waiting for content warnings or edit-blocks). People who appear to be using PatchChat for the primary purpose of harassing others may be banned without warning.</li>
    </ul>
</p>

<h3>Block first before reporting violations</h3>
    
<p>If you feel that another person is violating this policy with respect to you or your content, you should block them in addition to reporting them to moderation.</p>

<h3>Banning users in extremist groups</h3>
    
<p>PatchChat will ban and delete all the content of any user who is a confirmed and/or declared member of any group on the <a href="http://www.publicsafety.gc.ca/cnt/ntnl-scrt/cntr-trrrsm/lstd-ntts/index-eng.aspx" target=_blank>Public Safety Canada List of Terrorist Entities</a>. This includes eco-extremist groups and those who would target, attack or otherwise harass Patchchat users.</p>



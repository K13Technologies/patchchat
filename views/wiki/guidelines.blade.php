<h3>How To Write a Wiki Page</h3>

<OL>
	<li>Use a neutral point of view.</li>
	<li>Be objective, this is not
	personal.</li>
	<li>Simplify your language where
	possible.</li>
	<li>Post in a way consistent with
	&ldquo;normal writing&rdquo;. That is members should not post
	excessive numbers of emoticons or colored text, etc. Members should
	not SHOUT or use excessive punctuation (e.g. ! and ?) in topic
	titles or posts.</li>
	<li>A well article is written with an
	&ldquo;introduction&rdquo;, &ldquo;body&rdquo; and &ldquo;conclusion&rdquo;
	format.</li>
	<li>Edit/proofread your post, and fix
	your formatting.</li>
	<li>Use a clear descriptive title.</li>
</OL>

<h3>How Not to Write
a Wiki Page</h3>
<OL>
	<li>Do not use poor
	grammar or incorrect spelling.</li>
	<li>Do not use bad
	English.</li>
	<li>Do not use
	contractions (I&rsquo;ve, can&rsquo;t, hasn&rsquo;t). Instead, use
	the long forms of the words.</li>
	<li>Do not use
	complex sentences.</li>
	<li>Do not use
	idioms (One or more words that together mean something other than
	what they say).</li>
	<li>Do not use
	words you are not sure about with using a dictionary or thesaurus.</li>
	<li>Do not write in
	the second person. A good wiki page is never addressed to &ldquo;you&rdquo;.</li>
	<li>Do not use
	slang; many of our readers will not be familiar with slang and or
	any other variations of a word.</li>
</OL>

<p><B>Spelling Used On a Wiki Page </B>
</P>

<p>The  spelling  used
in the Simple English Wikipedia can be the spelling of any valid
version of English. Two common versions are <A HREF="https://simple.wikipedia.org/wiki/British_English">British
English</A> and <A HREF="https://simple.wikipedia.org/wiki/American_English">American
English</A>.</p>
<p>Different spellings
should not be mixed in an article. It will look bad if one spelling
is mixed with another. It is best to write in the spelling you are
used to. If you write in a spelling you aren't used to, you might not
know that the grammar is also different sometimes.</p>
<p>For members on .CA
website use British English, for our members on our .US website use
American English.</p>
<h3>Citing Your
Sources</h3>
<p>It is important to
cite your sources that you might have used in authoring a PatchChat
Wiki. In the event that you did use a source besides your own
expertise and knowledge please read the following first.</p>
<OL>
	<li>Do not use
	sources, which copied their information from Wikipedia
	(<A HREF="http://www.wikipedia.org">www.wikipedia.org</A>) also, do
	not use Wikipedia itself (in any language) as a source.</li>
	<li>Do not use
	sources such as newspapers.</li>
	<li>Do not use
	Quora (<A HREF="http://www.quora.com">www.quora.com</A>) as a
	source.
	</li>
	<li>You should
	always add a reference when quoting published material. You should
	always include the page number if there is one. The reference should
	be placed either right after the quotation</li>
</OL>

<p>              <I>Example:</I>  &quot;This
is a quotation&quot;.<SUP>[1]</SUP></P>

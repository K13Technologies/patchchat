<h3>What is Wiki Verification?</h3>

<p>After a Wiki page
has been authored it is subject to verification from the PatchChat
community. This unique feature ensures that content authored is
verified by other professionals working in the same industry. This
will remove subjective, opinionated, biased and writer centric Wiki
content.</p>

<p>When the PatchChat
community has verified a Wiki page. The Wiki page becomes protected
and will become a verified resource reference for other readers to
utilize. This feature will showcase the knowledge of our PatchChat
members and strengthen the quality of our community.</p>


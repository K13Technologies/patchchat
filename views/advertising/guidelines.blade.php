<p>Advertisers must be honest about the product or service their ad promotes; they must avoid content that misleads or offends; and they must never compromise our users' privacy. The following specific rules apply to all advertisers:</p>

<h3>Applicable Guidelines</h3>

<p>All advertisements on PatchChat must comply with these Advertising Guidelines and with PatchChat’s Terms of Use, Community Guidelines, and Privacy Policy. For political advertisements, advertisers also must comply with PatchChat’s Political Advertising Guidelines. These terms, policies, and guidelines may be changed without notice.</p>

<h3>Review</h3>

<p>All ads are subject to PatchChat’s review and approval. We reserve the right to reject any ad in our sole discretion for any reason. We also reserve the right to request modifications to any ad, and to require factual substantiation for any claim.</p>

<h3>Prohibited Ads</h3>

<p>Ads may not violate any applicable laws or regulations or the intellectual property, privacy, publicity, copyright, or other legal rights of any person. Nor may they be false, misleading, fraudulent, defamatory, or deceptive. Ads for the following products and services are prohibited:</p>
<ul>
<li>Adult products and services (other than contraceptives; see below); </li><li>Cigarettes (including e-cigarettes), cigars, smokeless tobacco, and other
tobacco products;
</li><li>Products or services that bypass copyright protection, such as software or
cable signal descramblers;
</li><li>Products or services principally dedicated to selling counterfeit goods or
engaging in copyright piracy;
</li><li>Get-rich-quick or pyramid schemes or offers or any other deceptive or
fraudulent offers;
</li><li>Illegal or recreational drugs or drug paraphernalia; </li><li>Counterfeit, fake or bootleg products, or replicas or imitations of designer
products;
</li><li>Ads that promote particular securities or that provide or allege to provide
insider tips; and
</li><li>Any illegal conduct, product, or enterprise.</li>
</ul>

<p>The following ad content is prohibited:</p>
<ul>
<li>Content that demeans, degrades, or shows hate toward a particular race,
gender, culture, country, belief, or toward any member of a protected class;
</li><li>Content that exploits an individual because they are drunk or otherwise
intoxicated;
</li><li>Content depicting nudity, sexual behaviour, or obscene gestures; </li><li>Content depicting drug use; </li><li>Content depicting excessive violence, including the harming of animals; </li><li>Shocking, sensational, or disrespectful content; </li><li>Deceptive, false, or misleading content, including deceptive claims, offers, or
business practices;
</li><li>Content that directs users to phishing links, malware, or similarly harmful
codes or sites; and
</li><li>Content that deceives users into providing personal information without
their knowledge, under false pretences, or to companies that resell, trade, or otherwise misuse that personal information.</li>
</ul>

<h3>Restricted ads</h3>
<p>Ads in the following categories are restricted and will require individual approval on a case-by-case basis:</p>
<ul>
<li>Ads that promote or reference alcohol. Such ads must comply with applicable
laws and industry codes. They also must be designed to appeal to, and must
be targeted to, an appropriate age demographic;
</li><li>Ads for online dating services; </li><li>Ads for gambling and games of skill; </li><li>Lotteries run by government entities. These ads are generally permissible,
provided that they're targeted in accordance with applicable law;
</li><li>Ads for financial services; </li><li>Ads for contraceptives; </li><li>Ads for online pharmacies or pharmaceuticals; and </li><li>Ads promoting dietary and herbal supplements.</li>
</ul>

<h3>Testimonials & Endorsements</h3>
<p>Any testimonials and endorsements contained in ads must comply with all applicable laws and regulations, including any applicable Federal Trade Commission/Canadian Trader Commission Service guidelines and requirements. For example, a clear and conspicuous disclaimer is required if an endorser's results are atypical or if the endorser was paid.</p>


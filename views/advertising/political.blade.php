<p>Political advertising that appears on PatchChat has to be right for our users, and it has to be lawful.</p>

<h3>The bottom line</h3>
<p>Political advertising must comply with all applicable laws, including all federal election laws, federal regulations, copyright law, defamation law, and (where applicable) other provincial or local laws and regulations.</p>
<p>Complying with those laws will be the sole responsibility of the advertiser. Advertisers will be asked to certify in their contract with PatchChat that their ads indeed comply with all applicable laws, and to indemnify PatchChat for any claims to the contrary.</p>
<p>All political advertising must clearly identify who paid for the communication and whether or not the ad was authorized by a candidate and/or organization.</p>
<p>Unauthorized ads must also include contact information for the sponsoring organization. Political advertising must not be misleading or deceptive. Nor may it violate <a href="{{ route("terms") }}">PatchChat’s Terms of Use</a>, <a href="{{ route("community.guidelines")}}">Community Guidelines</a>, and <a href="{{ route("advertising.guidelines") }}">Advertising Guidelines.</a> That means, among other things:</p>
<ul>
<li>No content that harasses, intimidates, threatens, or ridicules.</li>
<li>No content that impersonates any person or entity or otherwise misrepresents your affiliation with a person or entity.</li>
<li>No content that violates the publicity, privacy, copyright, or other intellectual
property rights of a third party.</li>
<li>No content that features graphic violence.</li>
<li>No content that features recreational drugs, nudity, obscenity, or anything
else that violates our Advertising Guidelines.</li>
</ul>

<p>See the documents linked above for further details.</p> 
<p>We encourage political advertisers to be positive. But we don't categorically ban “attack” ads; expressing disagreement with or campaigning against a candidate or party is generally permissible if it meets our other guidelines. That said, political ads must not include attacks relating to a candidate's personal life.</p>

<h3>PatchChat’s rights</h3>

<p>PatchChat will review political ads on a case-by-case basis.</p>

<p>We reserve the right to reject in our sole discretion, or request modifications to, ads that we believe violate the standards listed above or that are otherwise inappropriate. Our discretion will never be exercised with the intent to favour or disfavour any candidate or political party. We also reserve the right to require substantiation of an advertiser's factual claims.</p>

<h3>Political advertising for non-Canadian campaigns</h3>

<p>Political ads for electoral campaigns outside Canada will be considered on a case-by-case basis at PatchChat’s discretion. Any such ads must meet all legal requirements for that jurisdiction, as well as all of the above requirements.</p>

